public class ContainString {
    
    public static void main(String[] args) {
        String name = "Welcome to Muhamad Rizki Akbar";
        System.out.println("Hasilnya : " +name.contains("Muhamad Rizki Akbar"));
        System.out.println("Hasilnya : " +name.contains("is"));
        System.out.println("Hasilnya : " +name.contains("here"));
    }
}
